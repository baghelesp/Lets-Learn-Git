import java.util.*;
public class Difference {
    public static void main(String[] args) {
        Scanner sc= new Scanner(System.in);
        System.out.print("Enter first integer : ");
        int a= sc.nextInt();
        System.out.print("Enter Second integer : ");
        int b=sc.nextInt();
        int c= a-b;
        System.out.println(a+" - "+b+" = "+c);
    }
}
